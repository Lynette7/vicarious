export function getContinent(countryCode: string): string {
  return CONTINENT_MAP[countryCode] || 'Unknown';
}

export function getCountriesInContinent(continent: string): string[] {
  return Object.entries(CONTINENT_MAP)
    .filter(([_, cont]) => cont === continent)
    .map(([code]) => code);
}
